

// Regular Function 

function greeting(firstName, lastName){
  console.log('Hello ' + firstName + ' ' + lastName + ' how are you? ' + "I'm coming !!!");
};

greeting('Braja', 'Sifa');
greeting('DumbWays', 'Bootcamp');

// Arrow Function

let say = (firstName, lastName) => {
  console.log('Hello ' + firstName + lastName + ' how are you?' + " I'm coming!!!");
};

say('Braja', 'Sifa');
say('DumbWays', 'Bootcamp');